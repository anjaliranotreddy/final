public class AddSubtract {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;

        // Addition
        int sum = num1 + num2;
        System.out.println("Sum: " + sum);

        // Subtraction
        int diff = num1 - num2;
        System.out.println("Difference: " + diff);
    }
}

